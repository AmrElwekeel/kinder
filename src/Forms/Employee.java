    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int Row = jTable1.getSelectedRow();
        txtId.setText(jTable1.getValueAt(Row, 0).toString());
        txtName.setText(jTable1.getValueAt(Row, 1).toString());
        txtAddres.setText(jTable1.getValueAt(Row, 2).toString());
        txtPayments.setText(jTable1.getValueAt(Row, 3).toString());
        txtPhone.setText(jTable1.getValueAt(Row, 4).toString());
        
       
    }//GEN-LAST:event_jTable1MouseClicked

