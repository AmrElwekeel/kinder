   private void SetClear(){
       Tools.ClearText(this);
       txtId.setText(t.getAutoNumber());
      
       t.getAllRow(jTable1);
       
   
   
   }
    private void SetValues(){     
       

      t.setIdTeacher(Integer.parseInt(txtId.getText()));
      t.setName(txtName.getText());
      t.setAddres(txtAddres.getText());
      t.setPayments(Double.parseDouble(txtPayments.getText()));
      t.setPhone(txtPhone.getText()); 
      txtId.setText(t.getAutoNumber());
    }
    
